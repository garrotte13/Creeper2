function fireutil.make_color (r, g, b, a)
  return { r = r * a, g = g * a, b = b * a, a = a }
end


function fireutil.foreach(table_, fun_)
  for k, tab in pairs(table_) do
    fun_(tab)
    if tab.hr_version then
      fun_(tab.hr_version)
    end
  end
  return table_
end


